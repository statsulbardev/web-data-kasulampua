                    </p>
                    <p class="text-dark">  
                    Produk Domestik Regional Bruto (PDRB) merupakan salah satu perhitungan statistik yang sangat berguna dalam mendukung perencanaan pembangunan ekonomi daerah. “Melalui PDRB kita dapat mengukur kemampuan sumberdaya wilayah, sektor-sektor yang dominan, sektor-sektor yang potensial, sumber-sumber pertumbuhan ekonomi dan laju pertumbuhan ekonomi wilayah”, tutur Isran. Dalam keterangannya ia menjelaskan “Jika kita perhatikan struktur PDRB regional Kalimantan, terlihat bahwa perekonomian provinsi-provinsi di Kalimantan didominasi oleh sektor primer, terutama Lapangan Usaha Pertambangan dan Penggalian dan Lapangan Usaha Pertanian”. Hal ini tentunya merupakan tantangan besar bagi arah kebijakan pembangunan nasional di wilayah regional Kalimantan yakni menjadikan wilayah Kalimantan sebagai pusat produksi dan pengolahan hasil tambang, kehutanan dan lumbung energi nasional. 
                    </p>
                    <p class="text-dark">
                    Isran berharap Rakonreg PDRB dapat merumuskan kebijakan khusus, baik berupa peraturan perundangan maupun insentif fiskal dan non fiskal untuk mendorong perkembangan industri pengolahan di Pulau Kalimantan. Terutama pada wilayah-wilayah pusat pertumbuhan yang telah ada di masing-masing provinsi, baik kawasan yang berbentuk Kawasan Ekonomi Khusus maupun kawasan-kawasan industri yang telah ditetapkan.  Secara Khusus, Isran menegaskan bahwa Provinsi KalimantanTimur memiliki posisi strategis di regional Kalimantan, karena memberikan kontribusi 51,93 persen terhadap PDRB Regional Kalimantan. Sementara laju pertumbuhan ekonomi Kalimantan Timur pada tahun 2018 adalah 2,67 persen dan merupakan yang terendah di regional Kalimantan. Perlambatan pertumbuhan PDRB Kalimantan Timur akan memberikan dampak negatif besar terhadap pencapaian target pertumbuhan ekonomi regional Kalimantan.
                    </p>
                    <p class="text-dark">
